namespace _3DObjectViewer.Core.Models;

/// <summary>
/// Specifies the application theme mode.
/// </summary>
public enum AppTheme
{
    /// <summary>
    /// Follow the Windows system theme setting.
    /// </summary>
    System,

    /// <summary>
    /// Use light theme regardless of system setting.
    /// </summary>
    Light,

    /// <summary>
    /// Use dark theme regardless of system setting.
    /// </summary>
    Dark
}
